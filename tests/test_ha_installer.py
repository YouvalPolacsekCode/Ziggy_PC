def test_apply_happy_path(tmp_path, monkeypatch):
    state_path = _redirect_state(monkeypatch, tmp_path)
    f = tmp_path / "compose.yml"
    f.write_text(_compose_with("2026.4.2"))
    seen_marked: list[dict] = []

    r = ha_installer.apply_manifest(
        _manifest("2026.5.1"),
        settings=_settings(f),
        _run_cmd=_FakeRun(returncode=0),
        _http_get=lambda *a, **kw: _FakeResp(200, {"version": "2026.5.1"}),
        _sleep=lambda s: None,
        _mark_installed=lambda m: seen_marked.append(m),
    )
    assert r["ok"] is True
    assert r["reason"] == "installed"
    assert r["from_version"] == "2026.4.2"
    assert r["to_version"] == "2026.5.1"
    assert r["rolled_back"] is False
    assert seen_marked and seen_marked[0]["release_id"] == 7
    # Compose actually pinned
    assert f"{_HA_REPO}:2026.5.1" in f.read_text()
    # Installer state: previous cleared after success
    saved = json.loads(state_path.read_text())
    assert saved["previous_image_tag"] is None
    assert "installed" in saved["last_apply_outcome"]


