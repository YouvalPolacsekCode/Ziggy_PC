def test_apply_recreate_failure_triggers_rollback(tmp_path, monkeypatch):
    _redirect_state(monkeypatch, tmp_path)
    f = tmp_path / "compose.yml"
    f.write_text(_compose_with("2026.4.2"))

    # First call (apply's `up -d`) fails; second call (rollback's `up -d`)
    # succeeds. _FakeRun returns the same code each call, so use a counter.
    calls = {"n": 0}
    def fake_run(cmd, *, timeout):
        calls["n"] += 1
        rc = 1 if calls["n"] == 1 else 0
        return subprocess.CompletedProcess(cmd, rc, stdout="", stderr="boom")

    # Health probe is irrelevant for apply (recreate fails first) but
    # rollback's alive-probe must succeed.
    r = ha_installer.apply_manifest(
        _manifest("2026.5.1"),
        settings=_settings(f),
        _run_cmd=fake_run,
        _http_get=lambda *a, **kw: _FakeResp(200, {"version": "2026.4.2"}),
        _sleep=lambda s: None,
    )
    assert r["ok"] is False
    assert "recreate_failed" in r["reason"]
    assert r["rolled_back"] is True
    # Compose reverted
    assert f"{_HA_REPO}:2026.4.2" in f.read_text()


def test_apply_probe_failure_triggers_rollback(tmp_path, monkeypatch):
    _redirect_state(monkeypatch, tmp_path)
    f = tmp_path / "compose.yml"
    f.write_text(_compose_with("2026.4.2"))

    # docker compose always succeeds. Apply's version probe never matches
    # the new version. Rollback's alive probe returns 200.
    def fake_get(url, headers=None, timeout=None):
        # Always return the OLD version — apply expects new and never sees it
        return _FakeResp(200, {"version": "2026.4.2"})

    r = ha_installer.apply_manifest(
        _manifest("2026.5.1"),
        settings=_settings(f, attempts=2, interval=0.0),
        _run_cmd=_FakeRun(returncode=0),
        _http_get=fake_get,
        _sleep=lambda s: None,
    )
    assert r["ok"] is False
    assert "health_probe_failed" in r["reason"]
    assert r["rolled_back"] is True
    assert f"{_HA_REPO}:2026.4.2" in f.read_text()


