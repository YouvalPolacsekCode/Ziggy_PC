"""Tests for services/ha_installer.py — Prompt 4 chunks 1.C + 1.D.

Coverage:
  read_current_image     happy path + missing file + 0 / multiple matches
  pin_compose_image      happy path + atomic write + repo mismatch refuses
                         + preserves comments around the image line
  recreate_ha_container  happy + non-zero exit + timeout + docker_not_found
  probe_ha_health        version match on first try + match after retries
                         + version mismatch exhausts attempts
                         + http error / network error retries
                         + malformed JSON retries
  probe_ha_alive         returns on first 200 regardless of version field
  rollback               happy path + compose already at target +
                         recreate failure + probe failure +
                         compose file missing
  apply_manifest         happy path (state.previous_image_tag flow,
                         mark_installed called, previous cleared)
                       + already_at_target (no mutation)
                       + dry_run (no mutation)
                       + invalid manifest shapes
                       + missing compose file (no rollback needed)
                       + compose_pin_failed (no rollback needed; file
                         was atomic)
                       + recreate failure → rollback fires
                       + probe failure → rollback fires
                       + rollback_on_failure=False keeps the failed state
                       + cascading: apply fails AND rollback fails
"""

