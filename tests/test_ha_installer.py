def test_apply_persists_previous_image_tag_before_mutation(tmp_path, monkeypatch):
    """Crash-safety: state.previous_image_tag is written BEFORE compose
    is mutated. We verify by injecting a pin failure and confirming the
    state file still records the previous tag."""
    state_path = _redirect_state(monkeypatch, tmp_path)
    f = tmp_path / "compose.yml"
    f.write_text(_compose_with("2026.4.2"))

    # Force pin_compose_image to fail AFTER load_state already ran.
    def boom(*a, **kw):
        raise ValueError("simulated pin failure")
    monkeypatch.setattr(ha_installer, "pin_compose_image", boom)

    r = ha_installer.apply_manifest(
        _manifest("2026.5.1"),
        settings=_settings(f),
        _run_cmd=_FakeRun(returncode=0),
    )
    assert r["ok"] is False
    assert r["reason"] == "compose_pin_failed"
    saved = json.loads(state_path.read_text())
    assert saved["previous_image_tag"] == "2026.4.2"
    assert saved["last_apply_outcome"] in (
        "failed_no_rollback: compose_pin_failed",
        "in_progress",   # tolerant — sequencing detail
    )
