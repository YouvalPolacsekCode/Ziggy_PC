def test_probe_version_match_first_try():
    calls: list[int] = []
    def fake_get(url, headers=None, timeout=None):
        calls.append(1)
        return _FakeResp(200, {"version": "2026.5.1"})
    r = ha_installer.probe_ha_health(
        "http://ha", "2026.5.1", attempts=3, interval_s=0.0,
        _http_get=fake_get, _sleep=lambda s: None,
    )
    assert r["ok"] is True
    assert r["actual_version"] == "2026.5.1"
    assert r["attempts_made"] == 1
    assert len(calls) == 1


def test_probe_version_match_after_retries():
    seq = [
        _FakeResp(503),
        _FakeResp(200, {"version": "2026.4.2"}),    # old version on first 200
        _FakeResp(200, {"version": "2026.5.1"}),
    ]
    i = iter(seq)
    r = ha_installer.probe_ha_health(
        "http://ha", "2026.5.1", attempts=5, interval_s=0.0,
        _http_get=lambda *a, **kw: next(i),
        _sleep=lambda s: None,
    )
    assert r["ok"] is True
    assert r["attempts_made"] == 3


def test_probe_version_mismatch_exhausts():
    r = ha_installer.probe_ha_health(
        "http://ha", "2026.5.1", attempts=3, interval_s=0.0,
        _http_get=lambda *a, **kw: _FakeResp(200, {"version": "2026.4.2"}),
        _sleep=lambda s: None,
    )
    assert r["ok"] is False
    assert "version_mismatch" in r["reason"]
    assert r["attempts_made"] == 3
    assert r["actual_version"] == "2026.4.2"


