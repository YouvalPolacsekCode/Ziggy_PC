_HA_REPO = "ghcr.io/home-assistant/home-assistant"


def _compose_with(tag: str) -> str:
    """Minimal but realistic compose. Comments around the image line let
    pin_compose_image's "preserves comments" assertion bite."""
    return f"""# Auto-generated by relay/app/provisioner.py
services:
  homeassistant:
    # Pin managed by services/ha_installer.py — do not edit by hand.
    image: {_HA_REPO}:{tag}
    restart: unless-stopped
  ziggy:
    image: registry.fly.io/ziggy:latest
"""


def _settings(compose_file: Path, *, health_url: str = "http://ha.local:8123",
              attempts: int = 2, interval: float = 0.0) -> dict:
    return {
        "home_assistant": {"token": "test-token"},
        "ha": {
            "compose_file":            str(compose_file),
            "image_repo":              _HA_REPO,
            "health_url":              health_url,
            "apply_timeout_s":         30,
            "health_check_attempts":   attempts,
            "health_check_interval_s": interval,
        },
    }


class _FakeResp:
    """requests-shaped fake. status_code + .json() + .text."""
    def __init__(self, status: int, payload=None):
        self.status_code = status
        self._payload = payload
        self.text = json.dumps(payload) if isinstance(payload, dict) else str(payload or "")

    def json(self):
        if isinstance(self._payload, dict):
            return self._payload
        raise ValueError("not json")


class _FakeRun:
    """subprocess.run-shaped fake. Configurable returncode + stderr."""
    def __init__(self, returncode: int = 0, stderr: str = "", stdout: str = "",
                 *, raise_exc: Optional[Exception] = None):
        self.returncode = returncode
        self.stderr = stderr
        self.stdout = stdout
        self.raise_exc = raise_exc
        self.calls: list[list[str]] = []

    def __call__(self, cmd, *, timeout):
        self.calls.append(list(cmd))
        if self.raise_exc is not None:
            raise self.raise_exc
        return subprocess.CompletedProcess(
            cmd, self.returncode, stdout=self.stdout, stderr=self.stderr,
        )


def _redirect_state(monkeypatch, tmp_path) -> Path:
    """Point the module-level installer state file at a temp path."""
    state = tmp_path / "ha_installer_state.json"
    monkeypatch.setattr(ha_installer, "INSTALLER_STATE_PATH", state)
    return state


